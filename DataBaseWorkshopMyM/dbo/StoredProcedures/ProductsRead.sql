CREATE PROCEDURE [dbo].[ProductsRead]
	@IDPRODUCTO INT = NULL
AS
BEGIN 
	SET NOCOUNT ON

	SELECT 
		IDPRODUCTO
	, CODIGO
  	, DESCRIPCION		  
	, UNIDAD
	, CANTIDADDISPONIBLE
	, PRECIOCOMPRA
	, PRECIOVENTA
	, COSTOTOTAL  
	FROM
	    dbo.PRODUCTS
	WHERE
	    (@IDPRODUCTO IS NULL OR IDPRODUCTO = @IDPRODUCTO)
	      

END
