CREATE PROCEDURE [dbo].[ProductsRead]
	@CODIGO VARCHAR(10)

AS
BEGIN 
	SET NOCOUNT ON

	SELECT 
	      CODIGO
  	, DESCRIPCION		  
	, UNIDAD
	, CANTIDAD_DISPONIBLE
	, PRECIO_COMPRA
	, PRECIO_VENTA
	, COSTO_TOTAL  
	FROM
	    dbo.PRODUCTS
	WHERE
	    (@CODIGO IS NULL OR CODIGO = @CODIGO)
	      

END
